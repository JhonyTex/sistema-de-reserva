            <li>Preferencias de alojamiento</li>
            <li>Información sobre su estancia (fechas de llegada y salida, solicitudes especiales)</li>
        </ul>

        <h4>2. Uso de la Información</h4>
        <p>Utilizamos la información recopilada para:</p>
        <ul>
            <li>Procesar y gestionar sus reservas y estancias</li>
            <li>Mejorar nuestros servicios y personalizar su experiencia</li>
            <li>Comunicarnos con usted sobre su reserva y enviarle confirmaciones</li>
            <li>Enviarle promociones y ofertas especiales, siempre que haya dado su consentimiento</li>
            <li>Realizar análisis para mejorar nuestros servicios y ofertas</li>
        </ul>

